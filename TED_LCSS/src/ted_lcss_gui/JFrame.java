    private void button_run_lcss_tab1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_run_lcss_tab1ActionPerformed
        // TODO add your handling code here:
        double input_e = 0;

        if ("insert double".equals(textarea_3.getText()) || "insert a file".equals(textarea_1.getText()) || "insert a file".equals(textarea_2.getText()))
        {
            dialog_error.setVisible(true);
        }
        else
        {
            input_e = Double.parseDouble(textarea_3.getText());
            file_1 = textarea_1.getText();
            file_2 = textarea_2.getText();
            
            textarea_result.setText("calculating...");
            textarea_result_clock.setText("calculating...");
            LCSS run = new LCSS(file_1,file_2,input_e);
            
            try {
                double lcss = 0;
                double min;
                
                /* run lcss */
                lcss = run.lcss();

                min = LCSS.line_counter_1;

                if (LCSS.line_counter_1 > LCSS.line_counter_2)
                {
                    min = LCSS.line_counter_2;
                }

                result = lcss/min;

                textarea_result.setText(String.valueOf(result*100) + " %");
                textarea_result_clock.setText(LCSS.clock.toString());
                
                flag_tab1 = 1;  //i will have coordinates_1,2
            } catch (IOException ex) {
                Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_button_run_lcss_tab1ActionPerformed

