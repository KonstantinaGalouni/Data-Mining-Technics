    private void button_run_lcss_tab2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_run_lcss_tab2ActionPerformed
        // TODO add your handling code here:
        double input_e = 0;
        int input_d = 0;

        if ("insert double".equals(textarea_6.getText()) || "insert integer".equals(textarea_7.getText()) || "insert a file".equals(textarea_5.getText()) || "insert a file".equals(textarea_4.getText()))
        {
            dialog_error.setVisible(true);
        }
        else
        {
            input_e = Double.parseDouble(textarea_6.getText());
            input_d = Integer.parseInt(textarea_7.getText());

            file_1 = textarea_5.getText();
            file_2 = textarea_4.getText();
            
            /* run lcss */
            textarea_result_tab2.setText("calculating...");
            textarea_result_clock_tab_2.setText("calculating...");
            
            final LCSS_d run_1 = new LCSS_d();
            double min;

            try {
                lcss = run_1.lcss(file_1, file_2, input_e, input_d);
            } catch (IOException ex) {
                Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            min = LCSS_d.line_counter_1;

            if (LCSS_d.line_counter_1 > LCSS_d.line_counter_2)
            {
                min = LCSS_d.line_counter_2;
            }

            result = lcss/min;

            textarea_result_tab2.setText(String.valueOf(result*100) + " %");
            textarea_result_clock_tab_2.setText(LCSS_d.clock_d.toString());
            
            flag_tab2 = 1;  //i will have coordinates_1,2
        }
        
    }//GEN-LAST:event_button_run_lcss_tab2ActionPerformed

